#include "Rtti.h"

//////////////////////////////////////////////////////////////////////////
/// < Forward Declares >


//////////////////////////////////////////////////////////////////////////
Rtti::Rtti(const char* a_cpName , const Rtti* a_pkBaseType)
	: m_cpName(a_cpName) , m_pkBaseType(a_pkBaseType)
{

}

//////////////////////////////////////////////////////////////////////////
const char* Rtti::GetName() const
{
	return m_cpName;
}

//////////////////////////////////////////////////////////////////////////
bool Rtti::IsExsactly(const Rtti& a_kType ) const
{
	return &a_kType == this;
}

//////////////////////////////////////////////////////////////////////////
bool Rtti::IsDerived(const Rtti& a_kType ) const
{
	const Rtti* pkSearch = this;

	while(pkSearch)
	{
		if(pkSearch == &a_kType)
		{
			return true;
		}
		pkSearch = pkSearch->m_pkBaseType;
	}
	return false;
}

